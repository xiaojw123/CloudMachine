package com.cloudmachine.bean;

import java.io.Serializable;

public class FenceInfo implements Serializable {

	


	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private double x;
	private double y;
	
	

	public double getX() {
		return x;
	}

	public void getX(double x) {
		this.x = x;
	}

	
	public double getY() {
		return y;
	}
	
	public void getY(double y) {
		this.y = y;
	}
	
	
	
	
}
