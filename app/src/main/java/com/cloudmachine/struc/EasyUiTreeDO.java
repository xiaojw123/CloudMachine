package com.cloudmachine.struc;

import java.util.ArrayList;
import java.util.List;

public class EasyUiTreeDO {

	private Long id;
	private String text;
	private boolean checked=false;
	
	private List<EasyUiTreeDO> children = new ArrayList<EasyUiTreeDO>();

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

	public boolean isChecked() {
		return checked;
	}

	public void setChecked(boolean checked) {
		this.checked = checked;
	}

	public List<EasyUiTreeDO> getChildren() {
		return children;
	}

	public void setChildren(List<EasyUiTreeDO> children) {
		this.children = children;
	}
}
	