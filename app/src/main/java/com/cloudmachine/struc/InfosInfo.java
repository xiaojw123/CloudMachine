public class InfosInfo implements Serializable{

    private long amount;
    private String limitInfo;
    private String expiryLife;
    private String couponId;
    private String name;

    public long getAmount() {
        return amount;
    }

    public void setAmount(long amount) {
        this.amount = amount;
    }

    public String getLimitInfo() {
        return limitInfo;
    }

    public void setLimitInfo(String limitInfo) {
        this.limitInfo = limitInfo;
    }

    public String getExpiryLife() {
        return expiryLife;
    }

    public void setExpiryLife(String expiryLife) {
        this.expiryLife = expiryLife;
    }

    public String getCouponId() {
        return couponId;
    }

    public void setCouponId(String couponId) {
        this.couponId = couponId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
