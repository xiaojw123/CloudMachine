package com.cloudmachine.struc;

import java.io.Serializable;
import java.util.List;

public class RolesTree implements Serializable{
	/**
	 * 
	 */
	private static final long serialVersionUID = -6190209970724195291L;
	private String name;
	private Long id;
	private List<RolesTree> children;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public List<RolesTree> getChildren() {
		return children;
	}
	public void setChildren(List<RolesTree> children) {
		this.children = children;
	}

}
